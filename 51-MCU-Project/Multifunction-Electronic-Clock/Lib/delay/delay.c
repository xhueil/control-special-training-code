/*
 *****************************************************************************************
 * 函数名称：delay1Ms
 * 函数功能：毫秒级别延时
 * 输入参数：xms:需要延时的时间
 * 输出参数：无
 * 其他说明：无
 ******************************************************************************************
 */
void delay1Ms(u16 xms)
{
	u16 x,y;
	for (x=xms;x>0;x--)
		for (y=114;y>0;y--);
}






